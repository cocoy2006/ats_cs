package molab.main.java.util.shell;

public class Top {

	private int pid;
	private String pr;
	private String cpu;
	private String s;
	private int thr;
	private String vss;
	private String rss;
	private String pcy;
	private String uid;
	private String name;

	/**
	 * @return the pid
	 */
	public int getPid() {
		return pid;
	}

	/**
	 * @param pid
	 *            the pid to set
	 */
	public void setPid(int pid) {
		this.pid = pid;
	}

	/**
	 * @return the pr
	 */
	public String getPr() {
		return pr;
	}

	/**
	 * @param pr
	 *            the pr to set
	 */
	public void setPr(String pr) {
		this.pr = pr;
	}

	/**
	 * @return the cpu
	 */
	public String getCpu() {
		return cpu;
	}

	/**
	 * @param cpu
	 *            the cpu to set
	 */
	public void setCpu(String cpu) {
		this.cpu = cpu;
	}

	/**
	 * @return the s
	 */
	public String getS() {
		return s;
	}

	/**
	 * @param s
	 *            the s to set
	 */
	public void setS(String s) {
		this.s = s;
	}

	/**
	 * @return the thr
	 */
	public int getThr() {
		return thr;
	}

	/**
	 * @param thr
	 *            the thr to set
	 */
	public void setThr(int thr) {
		this.thr = thr;
	}

	/**
	 * @return the vss
	 */
	public String getVss() {
		return vss;
	}

	/**
	 * @param vss
	 *            the vss to set
	 */
	public void setVss(String vss) {
		this.vss = vss;
	}

	/**
	 * @return the rss
	 */
	public String getRss() {
		return rss;
	}

	/**
	 * @param rss
	 *            the rss to set
	 */
	public void setRss(String rss) {
		this.rss = rss;
	}

	/**
	 * @return the pcy
	 */
	public String getPcy() {
		return pcy;
	}

	/**
	 * @param pcy
	 *            the pcy to set
	 */
	public void setPcy(String pcy) {
		this.pcy = pcy;
	}

	/**
	 * @return the uid
	 */
	public String getUid() {
		return uid;
	}

	/**
	 * @param uid
	 *            the uid to set
	 */
	public void setUid(String uid) {
		this.uid = uid;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
